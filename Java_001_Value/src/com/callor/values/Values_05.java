package com.callor.values;

public class Values_05 {

	public static void main(String[] arg) {

		// 코드에서 동등연산자(==)
		// 어떤 값이 같은지?
		// 값이 같으면 결과는 true, 다르면 결과는 false
		System.out.println("3 과 3 = " + (3 == 3) );
		System.out.println("4 과 3 = " + (4 == 3) );
		System.out.println("5 과 3 = " + (5 == 3) );
		
		System.out.println("3 > 3 = " + (3 > 3) );
		System.out.println("3 < 3 = " + (3 < 3) );
		
		System.out.println("4 > 3 = " + (4 > 3) );
		System.out.println("4 < 3 = " + (4 < 3) );
		
		System.out.println("3 >= 3 = " + (3 >= 3) );
		System.out.println("3 <= 3 = " + (3 <= 3) );
		
		System.out.println("4 >= 3 = " + (4 >= 3) );
		System.out.println("4 <= 3 = " + (4 <= 3) );
		
		
		// != : 값이 서로 같이 아니한가? (서로 다른가?)
		System.out.println("3 != 3 = " + (3 != 3) );
		// System.out.println("3 <> 3 = " + (3 <> 3) );
		
	}
}
