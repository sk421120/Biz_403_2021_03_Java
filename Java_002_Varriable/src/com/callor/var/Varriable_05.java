package com.callor.var;

public class Varriable_05 {
	
	public static void main(String[] args) {
		
		int intNum = 1;
		int intSum = 0;
		
		intSum += intNum;	//1
		
		intNum ++;			//2
		intSum += intNum;	//1 + 2
		
		intNum ++;			//3
		intSum += intNum;	//1 + 2 + 3
		
		intNum ++;			//4
		intSum += intNum;	//1 + 2 + 3 + 4
		
		intNum ++;			//5
		intSum += intNum;	//1 + 2 + 3 + 4 + 5
		
		intNum ++;			//6
		intSum += intNum;	//1 + 2 + 3 + 4 + 5 + 6
		
		intNum ++;			//7
		intSum += intNum;	//1 + 2 + 3 + 4 + 5 + 6 + 7
		
		intNum ++;			//8
		intSum += intNum;	//1 + 2 + 3 + 4 + 5 + 6 + 7 + 8
		
		intNum ++;			//9
		intSum += intNum;	//1 + 2 + 3 + 4 + 5 + 6 + 7 + 8 + 9
		
		
	}

}
